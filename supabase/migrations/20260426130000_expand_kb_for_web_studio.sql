insert into public.kb_categories (name, description)
select *
from (
  values
    ('Дизайн и UX', 'Вопросы о дизайне, прототипах, UI/UX и фирменном стиле.'),
    ('Разработка и технологии', 'Вопросы о стеке, CMS, фронтенде, бэкенде и интеграциях.'),
    ('Маркетинг и аналитика', 'Вопросы о SEO, аналитике, заявках и конверсии.'),
    ('Контент и наполнение', 'Вопросы о текстах, фото, структуре страниц и материалах от клиента.'),
    ('Оплата и договорённости', 'Вопросы о предоплате, этапах оплаты и формате работы.')
) as category(name, description)
where not exists (
  select 1
  from public.kb_categories existing
  where existing.name = category.name
);

